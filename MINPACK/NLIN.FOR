      SUBROUTINE FCN(X,F,N)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION X(3),F(3)
      COMMON/DAT/A1,C1,B1,B2,SIG1D
      XA1=X(1)
      XA2=X(2)
      XA3=X(3)
      F(1)=XA1**2+XA2**2+XA3**2-SIG1D**2
      F(2)=XA1**2+XA2**2+XA3**2+A1*XA2+A1*XA3-C1
      F(3)=XA1**2+XA2**2+XA3**2-B1*XA1-B2*XA2-B2*XA3-C1
      RETURN
      END
